print(df.isnull().sum())

df.dropna(inplace=True)

print(df.shape)

df.isnull().sum()

df = df[['id','articleType']]

df

top_categories = (
    df['articleType']
    .value_counts()
    .head(10)
    .index
)

df = df[
    df['articleType'].isin(top_categories)
]

print(df.shape)

image_dir = "/kaggle/input/fashion-product-images-small/images"

df['image_path'] = (
    image_dir + "/"
    + df['id'].astype(str)
    + ".jpg"
)

df = df[
    df['image_path'].apply(os.path.exists)
]

print(df.shape)

plt.figure(figsize=(10,8))

for i in range(9):

    plt.subplot(3,3,i+1)

